function triangles(number) {
  var symbol = ''
  for (var i = 0; i < number; i++) {
    symbol = symbol + '#';
    console.log(symbol);
  }
}
////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  for(var i = 1; i <= 15; i++) {
    if(i % 3 === 0 && i % 5 === 0) {
      console.log('fizzbuzz');
    } else if (i % 3 === 0) {
      console.log('fizz');
    } else if (i % 5 === 0) {
      console.log('buzz');
    } else {
      console.log(i);
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(number) {
  var board = "";
  for(var i = 0; i < number; i++){
    for(var a = 0; a < number; a++){
      board += (a % 2) === (i % 2) ? " " : "#";
    }
  board += "\n";
  }
  console.log(board);
}

